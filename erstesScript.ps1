# Definiere eine Variable für den Namen
# Setzt den Namen auf "Guts"
$name = "Guts"

# Definiere eine Variable für das Alter
# Setzt das Alter auf 26
$alter = 26

# Erstelle eine Begrüßung mit dem Namen
# Formatiert einen Begrüßungssatz mit dem festgelegten Namen
$begrüßung = "Hallo, mein Name ist $name."

# Erstelle eine Vorstellung mit Namen und Alter
# Formatiert einen Vorstellungssatz mit Namen und Alter
$vorstellung = "Ich bin $name und ich bin $alter Jahr(e) alt."

# Gib die Begrüßung aus
# Zeigt den Wert der Variable $begrüßung in der Konsole an
Write-Host $begrüßung

# Gib die Vorstellung aus
# Zeigt den Wert der Variable $vorstellung in der Konsole an
Write-Host $vorstellung

